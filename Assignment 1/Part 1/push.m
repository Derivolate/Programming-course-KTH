function stack = push(element, stack);

% Add an element on the top of a stack, and return the new stack. stack 
% is a cell array of Matlab strings and element is a Matlab string.
% BASTIAAN VANHOORN, MELANIE OBEREGGER, NOVEMBER 2021

% the top element is that of index (end) because we added elements of
% increasing index to the stack

stack =[stack element];

end
